import type { MetadataRoute } from "next";
import { getAllPosts } from "@/lib/blog";

const baseUrl = "https://meetup.momen.be";

function withAlternates(url: string) {
  return {
    languages: {
      "en": url,
      "x-default": url,
    },
  };
}

export default async function sitemap(): Promise<MetadataRoute.Sitemap> {
  const now = new Date();

  const staticPages: MetadataRoute.Sitemap = [
    { url: baseUrl, lastModified: now, changeFrequency: "weekly", priority: 1.0, alternates: withAlternates(baseUrl) },
    { url: `${baseUrl}/contact`, lastModified: now, changeFrequency: "monthly", priority: 0.7, alternates: withAlternates(`${baseUrl}/contact`) },
    { url: `${baseUrl}/legal`, lastModified: now, changeFrequency: "yearly", priority: 0.3, alternates: withAlternates(`${baseUrl}/legal`) },
    { url: `${baseUrl}/privacy`, lastModified: now, changeFrequency: "yearly", priority: 0.3, alternates: withAlternates(`${baseUrl}/privacy`) },
  ];

  const blogPages: MetadataRoute.Sitemap = getAllPosts().map((post) => ({
    url: `${baseUrl}/blog/${post.slug}`,
    lastModified: new Date(post.date),
    changeFrequency: "monthly" as const,
    priority: 0.6,
    alternates: withAlternates(`${baseUrl}/blog/${post.slug}`),
  }));

  const blogListing: MetadataRoute.Sitemap = [
    { url: `${baseUrl}/blog`, lastModified: now, changeFrequency: "weekly", priority: 0.7, alternates: withAlternates(`${baseUrl}/blog`) },
  ];

  return [...staticPages, ...blogListing, ...blogPages];
}
